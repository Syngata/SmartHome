
public class Garage {

	public void up() {
		System.out.println("Door is going up...");
	}
	
	public void down() {
		
		System.out.println("Door is going down...");
		
	}
	
	public void stop() {
		System.out.println("Door has stopped...");
	}
	
	public void lightsOn() {
		System.out.println("Lights are on ...");
		
	}
	
	public void lightsOff() {
		
		System.out.println("Lights are going off...");
	}
	
}
